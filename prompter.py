def get_user_msg(message):
    return {
        "role": "user",
        "content": message
    }


def get_assistant_msg(message):
    return {
        "role": "assistant",
        "content": message
    }


def system_message(marker):
    match marker:
        case "*":
            return sarcastic_system_message
        case ">":
            return friendly_system_message
        case "-":
            return doomer_system_message


